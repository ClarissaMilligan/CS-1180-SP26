public class VariablePractice
{
    public static void main(String[] args)
    {
        // variable declaration
        int numOfStudents;

        // variable assignment (initialization)
        numOfStudents = 8;

        System.out.println(numOfStudents);

        // combining declaration and assignment (initialization) into one line\
        // booleans can only hold true or false
        // true and false are java keywords
        boolean isPresent = true;

        isPresent = false;

        System.out.println(isPresent);

        // char can only hold a SINGLE number, letter, or symbol
        char letterGrade = 'A';
        System.out.println(letterGrade);
    }
}
